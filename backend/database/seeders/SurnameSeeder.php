    public function run(): void
    {
        Surname::query()->truncate();
        CountrySurname::query()->truncate();

        foreach (self::SURNAMES as $countrySurname => $surnames) {
            $country = Country::query()->where('name', $countrySurname)->first();
            foreach ($surnames as $surname => $frequency) {
                $surnameModel = Surname::query()->where(['surname' => $surname])->first();
                if (!$surnameModel) {
                    $surnameModel = new Surname();
                    $surnameModel->surname = $surname;
                    $surnameModel->save();
                }

                $model = new CountrySurname();
                $model->country_id = $country->id;
                $model->frequency = $frequency * 100;
                $model->surname_id = $surnameModel->id;
                $model->save();
            }
        }
    }
